#include "libft.h"

char	*ft_strtrim(char const *str, char const *set)
{
	int	start;
	int	end;

	start = 0;
	end = ft_strlen(str) - 1;
	while (ft_strchr((char *)set, str[start]))
		start++;
	while (ft_strchr((char *)set, str[end]))
		end--;
	return (ft_substr(str, start, (end - start) + 1));
}
